/*
**********************Decrement a Number with JavaScript**********************
You can easily decrement or decrease a variable by one with the -- operator.
Example

    i--;
is the equivalent of

    i = i - 1;

N/B: The entire line becomes i--;, eliminating the need for the equal sign.

*/
